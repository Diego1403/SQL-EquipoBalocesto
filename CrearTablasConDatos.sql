/* PRÁCTICA FINAL DE BBDD - BALONCESTO */

/* TABLAS */
/* 1 */
CREATE TABLE codigo_postal(
  cod_postal NUMBER (5) CONSTRAINT pk_cod_postal PRIMARY KEY,
  localidad VARCHAR (15) CONSTRAINT nn_localidad_cod NOT NULL
);
/* 2 */
CREATE TABLE entrenador(
  dni VARCHAR (9) CONSTRAINT pk_entrenador PRIMARY KEY,
  nombres VARCHAR (10) CONSTRAINT nn_nombre_entre NOT NULL,
  apellido VARCHAR (15) CONSTRAINT nn_apellido_entre NOT NULL,
  f_nacimiento DATE CONSTRAINT nn_f_nac_entre NOT NULL,
  cod_postal NUMBER (5) CONSTRAINT fk_cod_postal_entre REFERENCES codigo_postal
);
/* 3 */
CREATE TABLE club(
  cif VARCHAR (9) CONSTRAINT pk_club PRIMARY KEY,
  nombre VARCHAR (10) CONSTRAINT nn_nombre_club NOT NULL,
  anio_fundacion NUMBER (4) CONSTRAINT nn_anio_fund_club NOT NULL,
  valoracion_euros NUMBER (8) CONSTRAINT nn_valor_club NOT NULL,
  dni VARCHAR (9) CONSTRAINT fk_dni_entre_club REFERENCES entrenador
);
/* 4 */
CREATE TABLE jugador(
  dni VARCHAR (9) CONSTRAINT pk_jugador PRIMARY KEY,
  nombre VARCHAR (10) CONSTRAINT nn_nombre_jug NOT NULL,
  apellido VARCHAR (15) CONSTRAINT nn_apellido_jug NOT NULL,
  f_nacimiento DATE CONSTRAINT nn_f_nac_jug NOT NULL,
  altura NUMBER (3) CONSTRAINT nn_altura_jug NOT NULL,
  lesionado NUMBER (1) CONSTRAINT nn_lesionado_jug NOT NULL 
                       CONSTRAINT ck_lesionado_jug CHECK ( lesionado = 0 OR lesionado = 1),
  posicion VARCHAR (10) CONSTRAINT nn_posicion_jug NOT NULL
                       CONSTRAINT ck_posicion_jug CHECK (posicion = 'ESCOLTA' OR posicion = 'BASE' OR posicion = 'ALERO' OR posicion = 'ALA-PIVOT' OR posicion = 'PIVOT'),
  cif VARCHAR (9) CONSTRAINT nn_cif_club_jug NOT NULL
                  CONSTRAINT fk_cif_club_jug REFERENCES club
);
/* 5 */
CREATE TABLE estadio(
  nombre VARCHAR (10) CONSTRAINT nn_nombre_est NOT NULL,
  capacidad NUMBER (5) CONSTRAINT nn_capacidad_est NOT NULL,
  cod_postal NUMBER (5) CONSTRAINT fk_cod_postal_est REFERENCES codigo_postal,
  CONSTRAINT pk_nombre_cod_postal_est PRIMARY KEY (nombre, cod_postal)
);
/* 6 */
CREATE TABLE torneo(
  identificador NUMBER (4) CONSTRAINT pk_torneo PRIMARY KEY,
  nombre VARCHAR (30) CONSTRAINT nn_nombre_tor NOT NULL,
  premio NUMBER (6) CONSTRAINT nn_premio_tor NOT NULL
);
/* 7 */
CREATE TABLE tipo_partido(
  codigo VARCHAR (6) CONSTRAINT pk_tipo_partido PRIMARY KEY,
  descripcion VARCHAR (10) CONSTRAINT nn_descripcion_tipo_partido NOT NULL
);
/* 8 */
CREATE TABLE amistoso(
  codigo VARCHAR (6) CONSTRAINT pk_amistoso PRIMARY KEY
                     CONSTRAINT fk_cod_tipo_amis REFERENCES tipo_partido,
  descripcion VARCHAR (10) CONSTRAINT nn_descripcion_amis NOT NULL,
  causa VARCHAR (15) CONSTRAINT nn_causa_amis NOT NULL
                     CONSTRAINT ck_causa_amis CHECK (causa = 'BENEFICA' OR causa = 'ENTRENAMIENTO' OR causa = 'CELEBRACION')
);
/* 9 */
CREATE TABLE de_torneo(
  codigo VARCHAR (6) CONSTRAINT pk_de_torneo PRIMARY KEY
                     CONSTRAINT fk_cod_tipo_dtor REFERENCES tipo_partido,
  descripcion VARCHAR (10) CONSTRAINT nn_descripcion_dtor NOT NULL,
  estado VARCHAR (15) CONSTRAINT nn_estado_dtor NOT NULL
                     CONSTRAINT ck_estado_dtor CHECK (estado = 'FINAL' OR estado = 'SEMIFINAL'),
  identificador NUMBER (4) CONSTRAINT fk_id_torneo_dtor REFERENCES torneo
);
/* 10 */
CREATE TABLE clasificatorio(
  codigo VARCHAR (6) CONSTRAINT pk_clasificatorio PRIMARY KEY
                     CONSTRAINT fk_cod_tipo_clas REFERENCES tipo_partido,
  descripcion VARCHAR (10) CONSTRAINT nn_descripcion_clas NOT NULL,
  identificador NUMBER (4) CONSTRAINT fk_id_torneo_clas REFERENCES torneo
);
/* 11 */
CREATE TABLE club_partido(
  cif_1 VARCHAR (9) CONSTRAINT fk_cif1_club_par REFERENCES club,
  cif_2 VARCHAR (9) CONSTRAINT fk_cif2_club_par REFERENCES club,
  codigo VARCHAR (6) CONSTRAINT fk_codigo_tipo_club_par REFERENCES tipo_partido,
  fecha DATE,
  nombre VARCHAR (10),
  cod_postal NUMBER (5),
  puntos_club_1 NUMBER (3) CONSTRAINT nn_puntos1_club_par NOT NULL,
  puntos_club_2 NUMBER (3) CONSTRAINT nn_puntos2_club_par NOT NULL,
  ganador VARCHAR (9) CONSTRAINT fk_ganador_club_par REFERENCES club,
  CONSTRAINT pk_cif1_cif2_fecha_club_par PRIMARY KEY (cif_1, cif_2, fecha),
  CONSTRAINT fk_nombre_cod_postal_club_par FOREIGN KEY (nombre, cod_postal) REFERENCES estadio
);
/* 12 */
CREATE TABLE contrato(
  f_ini DATE,
  f_fin DATE CONSTRAINT nn_f_fin_contr NOT NULL,
  precio_compra NUMBER (10) CONSTRAINT nn_pre_compra_contr NOT NULL,
  precio_venta NUMBER (10) CONSTRAINT nn_pre_venta_contr NOT NULL,
  cif VARCHAR (9) CONSTRAINT fk_cif_contr REFERENCES club,
  dni VARCHAR (9) CONSTRAINT fk_dni_contr REFERENCES jugador,
  CONSTRAINT pk_f_ini_cif_dni_contr PRIMARY KEY (f_ini, cif, dni)
);

