/* VISTAS */
/* 1 - De cada club, cuantos partidos que ha ganado*/
CREATE OR REPLACE VIEW partidosganados (CLUB, PARTIDOS_GANADOS)
AS ( SELECT club.cif, COUNT(*) FROM club, club_partido WHERE (club_partido.ganador = club.cif) GROUP BY club.cif )
WITH READ ONLY;
/* 2 - Jugadores que midan más de 1.90*/
CREATE OR REPLACE VIEW jugadores190 (DNI,	Nombre,	Apellido,	F_nac, Altura, Lesionado, Posicion, CIF_club)
AS ( SELECT * FROM jugador WHERE (altura > 190) )
WITH CHECK OPTION;
/* 3 - De cada localidad , cuantos estadios hay*/
CREATE OR REPLACE VIEW localidadestadios (LOCALIDAD, NUMERO_DE_ESTADIOS)
AS (SELECT codigo_postal.localidad, COUNT(estadio.nombre) FROM estadio, codigo_postal WHERE codigo_postal.cod_postal = estadio.cod_postal GROUP BY codigo_postal.localidad)
WITH READ ONLY;
/* 4 - Clubes fundados en 1978 con los datos de su entrenador*/
CREATE OR REPLACE VIEW clubes1978 (CIF_CLUB, NOMBRE, VALORACION, DNI_ENTRENADOR, NOMBRE_E, APELLIDO)
AS (SELECT club.cif, club.nombre, club.valoracion_euros, entrenador.dni,  entrenador.nombres, entrenador.apellido FROM club, entrenador WHERE anio_fundacion = 1978 AND club.dni = entrenador.dni)
WITH READ ONLY;
/* 5 - Partidos amistosos bénifcos*/
CREATE OR REPLACE VIEW amistosos_beneficos (CLUB_1, CLUB_2, RESULTADO, ESTADIO)
AS (SELECT club_partido.cif_1, club_partido.cif_2, club_partido.puntos_club_1||'-'||club_partido.puntos_club_2, club_partido.nombre  FROM club_partido WHERE codigo = 'A1')
WITH READ ONLY;


